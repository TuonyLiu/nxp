int disk_flash_init(void)
{

    flexspi_config_t config;

    const clock_usb_pll_config_t g_ccmConfigUsbPll = {.loopDivider = 0U};

    CLOCK_InitUsb1Pll(&g_ccmConfigUsbPll);
    CLOCK_InitUsb1Pfd(kCLOCK_Pfd0, 24);   /* Set PLL3 PFD0 clock 360MHZ. */
    CLOCK_SetMux(kCLOCK_FlexspiMux, 0x3); /* Choose PLL3 PFD0 clock as flexspi source clock. */
    CLOCK_SetDiv(kCLOCK_FlexspiDiv, 2);   /* flexspi clock 120M. */
    SCB_DisableDCache();


    /*Get FLEXSPI default settings and configure the flexspi. */
    FLEXSPI_GetDefaultConfig(&config);

    /*Set AHB buffer size for reading data through AHB bus. */
    config.ahbConfig.enableAHBPrefetch = true;
    FLEXSPI_Init(EXAMPLE_FLEXSPI, &config);

    /* Configure flash settings according to serial flash feature. */
    FLEXSPI_SetFlashConfig(EXAMPLE_FLEXSPI, &deviceconfig, kFLEXSPI_PortA1);

    /* Update LUT table. */
    FLEXSPI_UpdateLUT(EXAMPLE_FLEXSPI, 0, customLUT, CUSTOM_LUT_LENGTH);

    return 0;
}

/*!
 * @brief  擦除一个扇区
 *
 * @param sector   扇区编号 从定义的起始地址开始算
 *
 * @retval 0x00    success.
 */
int disk_flash_erase(uint32_t sector)
{
    status_t status;

    uint32_t __sector = (FLASH_WRITE - FLASH_START)/SECTOR_SIZE + sector;
    
    
    status = flexspi_nor_flash_erase_sector(EXAMPLE_FLEXSPI, __sector * SECTOR_SIZE);
    if (status != kStatus_Success)
    {
        PRINTF("Erase sector failure !\r\n");
        return -1;
    }

    return status;
}

/*!
 * @brief  写一个扇区
 *
 * @param sector   扇区编号 从定义的起始地址开始算
 *
 * @retval 0x00    success.
 */
int disk_flash_write(uint32_t sector,void *buff)
{
    status_t status;

    uint32_t __sector = (FLASH_WRITE - FLASH_START)/SECTOR_SIZE + sector;
    uint8_t i = 0;

    /* Enter quad mode. */
    status = flexspi_nor_enable_quad_mode(EXAMPLE_FLEXSPI);
    if (status != kStatus_Success)
    {
        PRINTF("enable quad failure !\r\n");
        return status;
    }
    
    for (i=0;i<(SECTOR_SIZE/FLASH_PAGE_SIZE);i++) {
        
        status = flexspi_nor_flash_single_page_program(EXAMPLE_FLEXSPI, (__sector * SECTOR_SIZE+i*FLASH_PAGE_SIZE), (uint32_t *)(((uint32_t)buff+(i*FLASH_PAGE_SIZE))));  
        if (status != kStatus_Success)
        {
            PRINTF("Page program failure !\r\n");
            return -1;
        }
    }
    return status;

}

/*!
 * @brief  写一个扇区
 *
 * @param sector   扇区编号 从定义的起始地址开始算
 *
 * @retval 0x00    success.
 */
int disk_flash_read(uint32_t sector,void *buff)
{
    status_t status;

    uint32_t __sector = (FLASH_WRITE - FLASH_START)/SECTOR_SIZE + sector;

    status = flexspi_nor_fast_read_data(EXAMPLE_FLEXSPI, __sector * SECTOR_SIZE, (void *)buff);  
    if (status != kStatus_Success)
    {
        PRINTF("Page program failure !\r\n");
        return -1;
    }


}